
package com.samaham.warcardgame;
/**
 *
 * @author Matthew Samaha
 * This ANSI class provides a set og static string constants that represent ANSI color codes for output formatting.
 * The purpose of this is to add "life" into the static output in the console when playing the game.
 * Additionally, we wanted to implement this to add a visual boost and to make it easier to read output.
 */
public class ANSI {
    // ANSI color codes
    public static final String RESET = "\u001B[0m";
    public static final String BLACK = "\u001B[30m";
    public static final String RED = "\u001B[31m";
    public static final String YELLOW = "\u001B[33m";
    public static final String BLUE = "\u001B[34m";
}
